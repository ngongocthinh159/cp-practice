#define N 1000005
int divisor[N];
void pre_compute() {
    for (int i = 1; i < N; i++)
        for (int j = i; j < N; j += i) divisor[j]++;
}
void add(int &x, int y) {
    x += y;
    if (x >= MOD1) x -= MOD1;
}
void solve() {
    int n; cin >> n;
    n = 2 * n;
    vector<int> dp(n + 1);
    vector<int> pref(n + 1);
    dp[2] = 1;
    pref[2] = 1;
    for (int i = 4; i <= n; i += 2) {
        add(dp[i], pref[i - 2]);
        add(dp[i], divisor[i/2]);

        add(pref[i], dp[i]);
        add(pref[i], pref[i - 2]);
    }
    cout << dp[n] << nline;
}

