void cal(int &tot, int &k, int a, int x) {
    int p = a - x;
    int len = (k - p);
    if (len <= 0) return;
    int rep = len / x;
    tot += rep;
    k -= rep * x;
}
int solve2(int k, int a, int b, int x, int y) {
    int tot = 0;
    cal(tot, k, a, x);
    cal(tot, k, b, y);
    return tot;
}
