void solve() {
	cin >> n;
	for (int i = 0; i < n; i++) cin >> a[i];
	for (int i = 0; i < n; i++) dp[a[i]] = a[i];
	for (int i = 0; i < BIT; i++)
		for (int state = 0; state < (1 << BIT); state++) {
			if ((state >> i) & 1)
				dp[state] = max(dp[state], dp[state ^ (1 << i)]);
		}
	for (int i = 0; i < n; i++) {
		int ra = a[i] ^ ((1 << BIT) - 1);
		if (dp[ra]) 
			cout << dp[ra] << ' ';
		else
			cout << -1 << ' ';
	}
}

